// setData
// ----------------------------------------------------------------------------
void TxtFileFormat::setData(std::vector<uint8_t>dataNew)
{
    _data = dataNew;
}

// ----------------------------------------------------------------------------
// getData
// ----------------------------------------------------------------------------
const std::vector<uint8_t>TxtFileFormat::getData() const
{
    return _data;
}
