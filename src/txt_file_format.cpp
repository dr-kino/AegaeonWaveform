// ----------------------------------------------------------------------------
// setFileName
// ----------------------------------------------------------------------------
void TxtFileFormat::setFileName(std::string fileNameNew)
{
    _fileName = fileNameNew;
}

// ----------------------------------------------------------------------------
// getFileName
// ----------------------------------------------------------------------------
